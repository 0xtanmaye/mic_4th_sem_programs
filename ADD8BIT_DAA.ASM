DATA SEGMENT
NUM1 DB 09H
NUM2 DB 03H
RESULT DB ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
        MOV AX, DATA
        MOV DS, AX

        MOV AL, NUM1
        MOV BL, NUM2
        ADD AL, BL
	DAA
        MOV RESULT, AL

        MOV AH, 4CH
        INT 21H
CODE ENDS
END START
