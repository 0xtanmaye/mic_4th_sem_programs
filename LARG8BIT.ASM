DATA SEGMENT
NUMS DB 13H, 69H, 33H, 57H, 09H
LARGEST DB 00H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
        MOV AX, DATA
        MOV DS, AX

        MOV SI, OFFSET NUMS
        MOV CL, 04H  ;IF CL=05H THEN DEC CL
        MOV AL, [SI]
        ;DEC CL
        ITER:
        INC SI
        CMP AL, [SI]
        JNB SKIP
        MOV AL, [SI]
        SKIP:
        DEC CL      ; LOOP ITER CAN BE USED INSTEAD OF THESE TWO
        JNE ITER    ; LINES TO AUTOMATICALLY DECREMENT AND JUMP TO ITER
        ;LOOP ITER

        MOV LARGEST, AL

        MOV AH, 4CH
        INT 21H
CODE ENDS
END START

